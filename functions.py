#como se declara una funcion?

#definicion de funcion:
def hacer_saludo():
    #lo que hace la funcion es imprimir un mensaje
    print("Hola, bienvenedio")

hacer_saludo()

#definicion de funcion con parametros
def saludar_nombre(nombre, apellido):
    #asignamos parametro al print
    print(f"Hola, bienvenido {nombre} {apellido}")

#llamamos ala funcion con el parametro
saludar_nombre("Kimberly", "Nunez")


